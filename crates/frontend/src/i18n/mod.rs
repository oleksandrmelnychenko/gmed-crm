use gloo_storage::{LocalStorage, Storage};
use leptos::prelude::*;

const LANG_KEY: &str = "gmed_lang";

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Lang {
    De,
    Ru,
}

impl Lang {
    pub fn code(&self) -> &'static str {
        match self {
            Lang::De => "de",
            Lang::Ru => "ru",
        }
    }

    pub fn label(&self) -> &'static str {
        match self {
            Lang::De => "Deutsch",
            Lang::Ru => "Русский",
        }
    }

    pub fn flag(&self) -> &'static str {
        match self {
            Lang::De => "🇩🇪",
            Lang::Ru => "🇷🇺",
        }
    }
}

fn load_lang() -> Lang {
    let stored = LocalStorage::get::<String>(LANG_KEY).unwrap_or_default();
    match stored.as_str() {
        "de" => Lang::De,
        _ => Lang::Ru,
    }
}

fn save_lang(lang: Lang) {
    let _ = LocalStorage::set(LANG_KEY, lang.code().to_string());
}

pub fn provide_i18n() -> (ReadSignal<Lang>, WriteSignal<Lang>) {
    let (lang, set_lang) = signal(load_lang());
    (lang, set_lang)
}

pub fn switch_lang(set_lang: WriteSignal<Lang>, lang: Lang) {
    save_lang(lang);
    set_lang.set(lang);
}

pub fn t(lang: Lang) -> &'static Translations {
    match lang {
        Lang::De => &de::TR,
        Lang::Ru => &ru::TR,
    }
}

